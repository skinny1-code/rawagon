'use strict';
const assert = require('assert');
const {savings,transition,feeDistInflow,yieldPerLTN} = require('../../packages/fee-distributor');
let p=0,f=0;
const t=(n,fn)=>{try{fn();console.log('  ✓',n);p++;}catch(e){console.log('  ✗',n,':',e.message);f++;}};
console.log('\n── fee-distributor ──');
t('savings: visa=$15K at $50K/mo 2.5%', ()=>{assert.strictEqual(savings(50000,500,2.5).visaAnnual,15000);});
t('savings: qwks cost < $10', ()=>{assert(savings(50000,500,2.5).qwksAnnual<10);});
t('savings: 9:1 customer ratio', ()=>{const s=savings(50000,500,2.5);assert(Math.abs(s.toCustomer/s.qwksFee-9)<0.01);});
t('savings: Stripe > Visa savings', ()=>{assert(savings(50000,500,3.2).netSaving>savings(50000,500,2.5).netSaving);});
t('transition: pStar formula', ()=>{const t2=transition(1499,100,0.084,0.12);assert(Math.abs(t2.ltnNeeded-Math.round(1499/(0.084*0.12)))<=1);});
t('transition: yield at P* = fee', ()=>{const t2=transition(1499,100,0.084,0.12);assert(Math.abs(t2.annualYieldAtTransition-1499)<1);});
t('feeDistInflow: 0.1%', ()=>{assert.strictEqual(feeDistInflow(3_444_000_000),3_444_000);});
t('yieldPerLTN: math correct', ()=>{assert(Math.abs(yieldPerLTN(3_444_000,40_000_000)-0.0861)<0.001);});
console.log(`\n  ${p}/${p+f} passed`); process.exit(f?1:0);
